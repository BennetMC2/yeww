/**
 * Supplements Knowledge section
 */
export function buildSupplementsKnowledgeSection(): string {
  return `<supplements_knowledge>
SUPPLEMENTS: REALISTIC VIEW

THE REALITY:
- $50+ billion industry with minimal regulation
- Most claims outpace evidence
- Quality varies wildly
- Marketing >> Science
- Most don't work as advertised

EVIDENCE HIERARCHY:
Most supplement claims come from mechanistic reasoning, animal studies, or small trials.
What we need: Large, long-term human trials with meaningful endpoints.
Most supplements have NOT passed this bar.

TIER 1 - STRONG EVIDENCE:

Vitamin D (if deficient):
- Good evidence for bone health, immune function
- Common deficiency (northern latitudes, dark skin, low sun)
- Dose: 1,000-4,000 IU/day depending on levels
- TEST FIRST: Get blood levels checked (aim 30-50 ng/mL)
- No benefit if not deficient

Creatine Monohydrate:
- One of most studied supplements
- Benefits: Muscle strength, power, possibly cognitive function
- Dose: 3-5g/day (no loading needed)
- Safe for healthy individuals
- May cause slight water retention initially

Omega-3s (EPA/DHA):
- Modest cardiovascular and anti-inflammatory benefits
- Best for those who don't eat fatty fish
- Dose: 1-3g EPA+DHA combined
- Food sources (salmon, mackerel) may be superior
- Quality matters (fish oil can oxidize)

Protein Powder (if needed):
- Just convenient protein delivery
- Use when whole food protein is impractical
- No magic—same protein from food works identically

TIER 2 - REASONABLE EVIDENCE:

Magnesium:
- Many people deficient
- Helps sleep, muscle function
- Forms: Glycinate (best absorbed, calming), citrate (good)
- Dose: 200-400mg/day

Caffeine (pre-workout):
- Strong for acute performance
- Dose: 3-6mg/kg body weight, 30-60 min before exercise
- Tolerance develops; affects sleep

Melatonin (for sleep timing):
- Good for jet lag and timing issues
- Dose: 0.5-3mg (lower often better)
- NOT a sleeping pill—helps with timing only

WEAK/MIXED EVIDENCE:

NAD+ precursors (NMN, NR):
- Strong rodent data, human evidence weak
- Expensive; premature to recommend

Resveratrol:
- Failed in most human trials despite hype
- Likely ineffective at achievable doses

Multivitamins:
- Large trials show no benefit in well-nourished populations
- Exception: Poor diets, absorption issues, specific needs

Antioxidant megadoses (Vitamin C, E):
- May interfere with exercise adaptation
- Some trials show harm
- Get antioxidants from food, not pills

BCAAs:
- If you eat adequate protein, BCAAs add nothing
- Unnecessary if protein intake is sufficient

Testosterone boosters (natural):
- Most ingredients show minimal or no effect
- Lifestyle factors matter more

QUALITY MATTERS:
- Look for third-party testing (NSF Certified for Sport, Informed Sport, USP)
- Avoid proprietary blends (hide actual doses)
- Avoid Amazon-only brands with no reputation
- Many products don't match labels

THE PHILOSOPHY: FOOD FIRST
1. Whole food nutrients work better (synergy, absorption)
2. Supplements can't replace good diet
3. Test for deficiencies before supplementing
4. Most supplement spending is wasted
5. Basics beat biohacks

WHEN SUPPLEMENTS MAKE SENSE:
- Confirmed deficiency (by blood test)
- Restricted diet (vegan B12, etc.)
- Increased needs (athletes, pregnancy, age)
- Convenience (protein powder when food impractical)
- Medical recommendation

BOTTOM LINE:
Money spent on quality food, good sleep, and consistent exercise will outperform almost any supplement stack.
</supplements_knowledge>`;
}
