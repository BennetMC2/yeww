/**
 * Nutrition Knowledge section
 */
export function buildNutritionKnowledgeSection(): string {
  return `<nutrition_knowledge>
NUTRITION FUNDAMENTALS

WHAT ACTUALLY MATTERS (Strong Evidence):
1. Not being overweight/obese
2. Eating adequate protein
3. Eating mostly whole foods
4. Not overeating
5. Staying hydrated

Everything else is marginal optimization.

ENERGY BALANCE:
- Calories in > out = weight gain
- Calories in < out = weight loss
- This is thermodynamics, not opinion
- Best diet = one you can stick to that creates appropriate balance

PROTEIN (Most Important Macro for Most People):
- Preserves muscle mass
- Most satiating macronutrient
- Higher thermic effect
- Critical for aging well

How much:
- Sedentary: 0.8g/kg body weight (minimum)
- Active adults: 1.6-2.2g/kg body weight
- Older adults: Higher end (anabolic resistance)
- Losing weight: Even higher (preserve muscle)

Distribute across meals (30-40g per meal) for optimal muscle protein synthesis.

FOOD QUALITY:
- Whole foods = foods that look like what they came from
- Higher satiety, better nutrients, beneficial fiber
- 80/20 rule: 80% whole foods, 20% can be flexible

FIBER (Often Overlooked):
- Feeds gut microbiome
- Improves satiety
- Regulates blood sugar
- Target: 25-35g/day (most get ~15g)
- Sources: vegetables, fruits, legumes, whole grains

WHAT PROBABLY DOESN'T MATTER MUCH:
- Specific diet labels (keto vs vegan vs paleo)
- Meal timing (for most people)
- Meal frequency (3 vs 6 meals)
- Eating before bed
- Superfoods
- Organic vs conventional

No diet has proven superior when calories and protein are matched.
Adherence beats optimization.

MACROS - REALISTIC VIEW:

Carbs: Not the enemy
- Primary fuel for high-intensity activity
- Quality matters (whole grains > refined)
- Low-carb works for some, not necessary for all

Fats: Also not the enemy
- Essential for hormones, absorption
- Prioritize unsaturated (olive oil, nuts, fish)
- Avoid industrial trans fats
- Omega-3s probably most evidence-backed

PRACTICAL PRINCIPLES:
- Protein source (palm-sized)
- Vegetables (half the plate)
- Complex carbs (fist-sized)
- Healthy fat (thumb-sized)

GOAL-SPECIFIC:

Fat loss:
- Create sustainable deficit (300-500 cal/day)
- Prioritize protein (preserve muscle)
- Increase fiber and vegetables
- Reduce liquid calories

Muscle gain:
- Modest surplus (200-300 cal/day)
- High protein (1.6-2.2g/kg)
- Distribute protein across meals
- Time carbs around training

Healthy aging:
- Protein becomes MORE important
- May need higher intake (2.0g/kg+)
- Focus on nutrient density

RED FLAGS - NUTRITION MISINFORMATION:
- Promises rapid results
- Demonizes entire food groups
- Relies on anecdotes over evidence
- Requires expensive supplements
- Uses "detox" or "cleanse" terminology
- One-size-fits-all solutions

The best nutrition advice is usually the most boring.
</nutrition_knowledge>`;
}
