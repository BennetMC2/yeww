/**
 * Stress & Nervous System Knowledge section
 */
export function buildStressKnowledgeSection(): string {
  return `<stress_knowledge>
STRESS & THE NERVOUS SYSTEM

THE STRESS RESPONSE:
When you perceive threat (physical or psychological), your HPA axis activates:
Hypothalamus → Pituitary → Adrenal glands → Cortisol + adrenaline

This increases heart rate, releases glucose, sharpens focus, suppresses "non-essential" functions (digestion, immune).

Adaptive for acute threats. Damaging when chronic.

ACUTE VS CHRONIC STRESS:
- Acute: Short-term, body recovers fully, healthy
- Chronic: Sustained activation, never fully recovers, damaging

CHRONIC STRESS CONSEQUENCES:
- Cardiovascular: High BP, heart disease risk
- Metabolic: Weight gain, insulin resistance
- Immune: Suppressed immunity, inflammation
- Mental: Anxiety, depression, cognitive decline
- Sleep: Insomnia, poor quality
- Aging: Accelerated cellular aging

HOW STRESS CONNECTS TO OTHER METRICS:

Stress → HRV: Chronic stress = lower HRV. Declining HRV often signals accumulated stress.
Stress → Sleep: Elevated cortisol disrupts sleep. Poor sleep increases stress. Vicious cycle.
Stress → Recovery: High stress = impaired recovery. Life stress + training stress = total load.

SIGNS OF STRESS ACCUMULATION:
- Persistent fatigue (even with sleep)
- Getting sick more often
- Emotional volatility
- Brain fog
- Declining performance
- Low motivation
- Sleep problems
- Cravings for comfort food

EVIDENCE-BASED STRESS MANAGEMENT:

Tier 1 (Strong):
1. Exercise—most powerful stress reducer
2. Sleep—fundamental to recovery
3. Social connection—relationships buffer stress

Tier 2 (Good):
4. Mindfulness/meditation—even 10 min helps
5. Nature exposure—lowers cortisol
6. Slow breathing—activates parasympathetic (6 breaths/min)

Supporting:
7. Limiting caffeine (amplifies stress response)
8. Reducing alcohol (disrupts sleep, worsens stress)
9. Time management (reduce sources of stress)

KEY INSIGHT:
Your nervous system tracks everything—work, relationships, training, sleep. It doesn't distinguish sources. Total stress load matters.

High life stress? Reduce training volume. The adaptation won't happen anyway.

WHEN TO SUGGEST PROFESSIONAL HELP:
- Signs of clinical anxiety or depression
- Burnout affecting function
- Physical symptoms with stress
- User expressing hopelessness
</stress_knowledge>`;
}
